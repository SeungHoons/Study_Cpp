void MAP::Init()
{

	for (int i = 0; i < HEIGHT; i++)
	{
		for (int j = 0; j < WIDTH; j++)
		{
			map[i][j] = (E_MAP)maptool[i][j];
		}
	}

	
}

void MAP::DrawMap()
{
	system("cls");

	for (int i = 0; i < HEIGHT; i++)
	{
		for (int j = 0; j < WIDTH; j++)
		{
			switch (map[i][j])
			{
			case EMPTY:
				cout << "  ";
				break;
			case BLOCK:
				cout << "бр";
				break;
			case ITEM:
				cout << "в╛";
				break;
			case PLAYER:
				cout << "бс";
				break;
			default:
				break;
			}
		}

		cout << endl;
	}
}


