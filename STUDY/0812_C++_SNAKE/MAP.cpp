void MAP::Init()
{
	srand((unsigned int)time(nullptr));

	for (int i = 0; i < HEIGHT; i++)
	{
		for (int j = 0; j < WIDTH; j++)
		{
			map[i][j] = (E_MAP)maptool[i][j];
		}
	}
	itemNum = 4;
	
}

void MAP::DrawMap()
{
	system("cls");
	RandomItem();
	for (int i = 0; i < HEIGHT; i++)
	{
		for (int j = 0; j < WIDTH; j++)
		{
			switch (map[i][j])
			{
			case EMPTY:
				cout << "  ";
				break;
			case BLOCK:
				cout << "бр";
				break;
			case ITEM:
				cout << "в╛";
				break;
			case PLAYER:
				cout << "бс";
				break;
			default:
				break;
			}
		}

		cout << endl;
	}
}

void MAP::RandomItem()
{

	if (itemNum > 0)
	{
		srand((unsigned int)time(nullptr));

		while (true)
		{
			int x = rand() % WIDTH;
			int y = rand() % HEIGHT;

			if (map[y][x] == EMPTY)
			{
				map[y][x] = ITEM;
				itemNum--;
				break;
			}
		}
	}


}


